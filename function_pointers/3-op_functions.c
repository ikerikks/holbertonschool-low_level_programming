#include "3-calc.h"

/**
 * op_add - sums two values
 * @a: integer
 * @b: integer
 *
 * Return: sum of two integers
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substracts two values
 * @a: integer
 * @b: integer
 *
 * Return: difference of two integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies two values
 * @a: integer
 * @b: integer
 *
 * Return: product of two integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides two values
 * @a: integer
 * @b: integer
 *
 * Return: division of two integers
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - gets the rest of a division
 * @a: integer
 * @b: integer
 *
 * Return: remainder of the division of two integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}

